    private void btnValidarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarDatosActionPerformed
        // TODO add your handling code here:
    String nombre = txtNombre.getText();
    String apellidos = txtApellidos.getText();
    String telefono = txtTelefono.getText();
    String email = txtEmail.getText();

    // Validar campos básicos
    if (nombre.isEmpty() || apellidos.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El nombre y los apellidos son obligatorios.");
        return;
    }

    // Validar teléfono: solo números y 8 dígitos
    if (!telefono.matches("\\d{8}")) {
        JOptionPane.showMessageDialog(this, "El teléfono debe tener 8 dígitos numéricos.");
        return;
    }

    // Validar email simple
    if (!email.contains("@") || !email.contains(".")) {
        JOptionPane.showMessageDialog(this, "El correo electrónico no parece válido.");
        return;
    }

    JOptionPane.showMessageDialog(this, "Los datos son válidos.");
    }//GEN-LAST:event_btnValidarDatosActionPerformed

