    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
  String nombre = txtNombre.getText();
    String apellidos = txtApellidos.getText();
    String telefono = txtTelefono.getText();
    String email = txtEmail.getText();
    String direccion = txtDireccion.getText();

    if (nombre.isEmpty() || apellidos.isEmpty() || telefono.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.");
        return;
    }

    // Crear la ventana que muestra la tabla
    TablaContactos contactos = new TablaContactos();

    // Agregar el nuevo contacto
    contactos.agregarContacto(nombre, apellidos, telefono, email, direccion);

    // Mostrar la ventana
    contactos.setVisible(true);

    // Limpiar campos
    txtNombre.setText("");
    txtApellidos.setText("");
    txtTelefono.setText("");
    txtEmail.setText("");
    txtDireccion.setText("");

    JOptionPane.showMessageDialog(this, "Contacto guardado y listado en la nueva ventana.");
    }//GEN-LAST:event_btnGuardarActionPerformed

